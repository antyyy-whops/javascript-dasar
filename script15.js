// Operasi Komparasi JavaScript

let angka1 = 10;
let angka2 = "10";

// equality (==)
console.log("10 == '10' :", angka1 == angka2);

// inequality (!=)
console.log("10 != '10' :", angka1 != angka2);

// strict equality (===)
console.log("10 === '10' :", angka1 === angka2);

// strict inequality (!==)
console.log("10 !== '10' :", angka1 !== angka2);

// lebih besar dari (>)
console.log("10 > 5 :", angka1 > 5);

// lebih besar sama dengan (>=)
console.log("10 >= 10 :", angka1 >= 10);

// lebih kecil dari (<)
console.log("10 < 5 :", angka1 < 5);

// lebih kecil sama dengan (<=)
console.log("10 <= 10 :", angka1 <= 10);
