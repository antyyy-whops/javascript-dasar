// Tipe Data String (anty)
var nama = "Aulia Putri";
console.log("Nama:", nama);

// Tipe Data Number (angka)
let umur = 17;
console.log("Umur:", umur);

// Tipe Data Boolean (true/false)
const sukaCoding = true;
console.log("Suka Coding:", sukaCoding);

// Tipe Data Null (kosong)
let nilaiKosong = null;
console.log("Nilai Kosong:", nilaiKosong);

// Tipe Data Undefined (belum diisi)
let belumDitentukan;
console.log("Belum Ditentukan:", belumDitentukan);

// Contoh gabung semua tipe data
console.log(
  "Halo, nama saya " +
    nama +
    ", umur saya " +
    umur +
    " tahun, dan " +
    "saya suka coding? " +
    sukaCoding
);
